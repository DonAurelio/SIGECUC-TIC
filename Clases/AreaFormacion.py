class AreaFormacion:

    def __init__(self, id_area_formacion, nombre):
        self.id_area_formacion = id_area_formacion
        self.nombre = nombre

    def getId_area_formacion(self):
        return self.id_area_formacion

    def getNombre(self):
        return self.nombre

    def setId_area_formacion(self, id_area_formacion):
        self.id_area_formacion = id_area_formacion

    def setNombre(self, nombre):
        self.nombre = nombre



