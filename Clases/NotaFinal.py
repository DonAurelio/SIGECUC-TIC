class NotaFinal:

    def __init__(self, Cohorte, notaFinal):
        self.Cohorte = Cohorte
        self.notaFinal = notaFinal

    def getCohorte(self):
        return self.Cohorte

    def getNotaFinal(self):
        return self.notaFinal

    def setCohorte(self, Cohorte):
        self.Cohorte = Cohorte

    def setNotaFinal(self, notaFinal):
        self.notaFinal = notaFinal



